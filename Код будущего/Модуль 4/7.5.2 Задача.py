# Создайте скрипт, который анализирует файл лога доступа и находит URL,
# запрашиваемые чаще заданного порогового значения.

#### Пояснение к задаче
# 1. Импортируйте класс `Counter` из модуля `collections`.
# 2. Откройте файл `url_access.log'` в режиме чтения и прочитайте все строки.
# 3. Создайте пустой список `urls` для хранения извлеченных URL.
# 4. Задайте пороговое значение (для определенности пусть это будет `10`) —
#    количество запросов, при превышении которого URL считается подозрительным.
# 5. Для каждой строки лога выполните следующие действия:
#    - Проверьте наличие символа `'"'` в строке.
#    - Разделите строку по символу `'"'` и извлеки вторую часть (содержит HTTP-запрос).
#    - Разделите HTTP-запрос на части с помощью `split()`.
#    - Если в запросе есть как минимум 2 части (метод и URL), добавьте URL (второй элемент) в список `urls`.
# 6. Создайте объект `Counter` из списка `urls` для подсчета частоты встречаемости каждого URL.
# 7. Переберите элементы счетчика. Если количество вхождений URL превышает заданный порог,
#    выведите сообщение в формате: `"URL {url} встречается {count} раз"`.

from collections import Counter

def analyze_url_frequency(filename):
    # 3. Список для хранения URL
    urls = []
    # 4. Пороговое значение
    threshold = 10
  
    try:
        # 2. Открытие и чтение файла
        with open(filename, 'r', encoding='utf-8') as file:
            for line in file:
                # 5. Извлечение URL
                if '"' in line:
                    # Разделяем по кавычкам, берем вторую часть (индекс 1)
                    parts = line.split('"')
                    if len(parts) > 1:
                        http_request = parts[1]
                        # Разделяем запрос (например, "GET /admin HTTP/1.1")
                        request_parts = http_request.split()
                        # Если есть метод и путь
                        if len(request_parts) >= 2:
                            url = request_parts[1]
                            urls.append(url)
      
        # 6. Подсчет частоты
        url_counts = Counter(urls)
      
        # 7. Проверка порога
        for url, count in url_counts.items():
            if count > threshold:
                print(f"URL {url} встречается {count} раз")
              
    except FileNotFoundError:
        print(f"Ошибка: Файл {filename} не найден")

if __name__ == "__main__":
    analyze_url_frequency('url_access.log')
