# В файле 2script.ps1 содержится PowerShell-скрипт.
# Некоторые строки в нём закодированы в Base64.
# Найдите и декодируйте эти строки.

#### Пояснения к задаче:
# Импортируйте модули re и base64.
# Откройте файл 2script.ps1 в режиме чтения текста, укажите кодировку utf-8 с пропуском некорректных символов.
# Считайте всё содержимое в переменную.
# С помощью регулярного выражения найдите все последовательности длиной 20 и более символов, состоящие из допустимых символов Base64 (буквы, цифры, +, /) и  возможных символов “равно” в конце строки (не более двух  = ).
# Длина строки выбрана 20 символов, чтобы отсеять короткие случайные последовательности.
# Для каждой найденной строки попробуйте декодировать её с помощью base64.b64decode().
# Если декодирование успешно, преобразуйте результат в строку UTF-8 (с игнорированием ошибок).
# В файле могут быть и другие Base64-строки, не только в явном виде — их тоже нужно найти и декодировать.
# Для каждой успешно декодированной строки выведите её порядковый номер, исходное Base64-представление и декодированный текст.

import re
import base64

def find_and_decode_base64(filename):
    # 1. Открываем файл с пропуском некорректных символов
    try:
        with open(filename, 'r', encoding='utf-8', errors='ignore') as f:
            content = f.read()
    except FileNotFoundError:
        print(f"Файл {filename} не найден.")
        return

    # 2. Регулярное выражение для Base64 (длиной 20+)
    # [A-Za-z0-9+/]{20,} — сами данные
    # ={0,2} — опциональное дополнение в конце
    pattern = r'[A-Za-z0-9+/]{20,}={0,2}'
  
    potential_base64 = re.findall(pattern, content)
  
    counter = 1
    for b64_str in potential_base64:
        try:
            # 3. Декодируем байты
            decoded_bytes = base64.b64decode(b64_str)
            # 4. Преобразуем в строку UTF-8
            decoded_text = decoded_bytes.decode('utf-8', errors='ignore')
          
            # Проверяем, что в результате не просто "мусор", а читаемый текст
            # (Минимальная проверка: есть ли хоть один печатный символ)
            if decoded_text.strip():
                print(f"Запись №{counter}")
                print(f"Исходный Base64: {b64_str}")
                print(f"Декодированный текст: {decoded_text}")
                print("-" * 30)
                counter += 1
        except Exception:
# Если это была просто случайная строка, похожая на Base64, пропускаем
            continue

if __name__ == "__main__":
    find_and_decode_base64("2script.ps1")
