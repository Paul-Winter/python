# Напишите функцию sql_escape(s), которая экранирует кавычки в строке для безопасного использования в SQL-запросах SQLite. Функция получает строку для экранирования, а возвращает строку с произведенными заменами. Правила экранирования:
# - Одинарная кавычка ' заменяется на две одинарные ''
# - Двойная кавычка " заменяется на две двойные ""

#### Пояснение к задаче
# 1. Если передана пустая строка, верните None (или пустую строку)
# 2. Сначала замените двойные кавычки, затем одинарные (чтобы не создать лишних замен)
#     - Верните преобразованную строку
#     - Пример: из строки 'Hello"world\'' функция должна сделать  'Hello""world''' (две двойные и две одинарные кавычки)

def sql_escape(s):
   # 1. Проверка на пустую строку или None
   if s is None or s == "":
       return None
  
   # 2. Экранирование двойных кавычек
   # Заменяем " на ""
   s = s.replace('"', '""')
  
   # 3. Экранирование одинарных кавычек
   # Заменяем ' на ''
   s = s.replace("'", "''")
  
   return s

# Примеры для самопроверки:
if __name__ == "__main__":
   test_str = "Hello\"world'"
   print(f"Original: {test_str}")
   print(f"Escaped:  {sql_escape(test_str)}")
   # Ожидаем: Hello""world''
