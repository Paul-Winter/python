# Считайте лог-файл, разберите его по строкам и найдите все аномальные входы, т.е. входы, которые не попадают в период от 8 до 16 часов

#### Пояснения к задаче:
# - Считайте файл log.txt
# - С помощью readlines считайте каждую строку файла
# - Пропустите пустые линии и лишние пробелы
# - Среди успешных входов возьмите для работы только символы времени (с 12 по 20 символ) и преобразуйте часы в число
# - Найдите имена пользователей и проверьте время входа
# - Выведите на экран какие пользователи вошли в неположенное время и во сколько этот вход произошел

# Читаем лог-файл
with open("log.txt", "r", encoding="utf-8") as file:
    for line in file:
        line = line.strip()
        if not line or len(line) < 20:
            continue
      
        try:
            # Извлекаем время (HH:MM:SS) по индексам
            time_str = line[11:19]
            hour = int(time_str[:2])
          
            # Находим имя пользователя более надежно
            # Делим строку по тире, убираем лишние пробелы у каждого куска
            parts = [p.strip() for p in line.split("-")]
          
            if len(parts) >= 2:
                username = parts[1]
              
               # Аномалия: до 8:00 или начиная с 16:00
                if hour < 8 or hour >= 16:
                    print(f"Пользователь {username} вошел в неположенное время: {time_str}")
                  
        except Exception:
            # Если строка не подошла, просто идём к следующей
            continue
