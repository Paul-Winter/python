# Напишите функцию isalphanumeric(s), которая проверяет, состоит ли переданная строка только из букв (латиница или кириллица) и цифр. Функция должна корректно обрабатывать пустую строку и None.

#### Пояснения к задаче: 
# 1. Не забудьте проверку на пустую строку, иначе при передаче None возникнет ошибка.
# 2. Для каждого символа проверьте, является ли он буквой или цифрой
# 3. Если все символы подходят, верните True, иначе False.

def isalphanumeric(s):
   # 1. Проверка на None и пустую строку
   if s is None or s == "":
       return False
  
   # 2. Проверка каждого символа (буквы любого алфавита + цифры)
   return s.isalnum()

# Примеры для самопроверки (исправлены кавычки):
if __name__ == "__main__":
   print(f"admin123: {isalphanumeric('admin123')}")
   # Используем двойные кавычки внутри f-строки, обрамленной одинарными
   print(f'admin": {isalphanumeric("admin")}')
   print(f"None: {isalphanumeric(None)}")
   print(f"Пусто: {isalphanumeric('')}")
