# Создайте уязвимый скрипт на Python с SQLite, который проверяет логин и пароль.
# Проверка должна быть оформлена в нем в виде функции login_to_system(user_login, user_password).
# Аутентификацию в нем должно быть можно обойти с помощью инъекции через через f-строку.
# Функция должны возвращать данные пользователя.

# В рамках этой задачи работайте с базой данных 4.3.1.db, где есть таблица users.
# В этой таблице есть пользователь admin с паролем secret123.

#### Пояснение к задаче
# 1. Подключитесь к базе данных 4.3.1.db с помощью sqlite3.connect()
# 2. Используйте conn.cursor() для выполнения запросов
# 3. Сформируйте небезопасный запрос (через f-строку), который подставляет введенные данные напрямую
# 4. Выполните запрос и возвратите данные пользователя с помощью return.

#### Устройство атак
# Напомним. как работают атаки такого типа.
# Чтобы обойти аутентификацию, атакующий при вводе логина используйте конструкцию: admin' –  (с пробелом после двух дефисов).
# Это закомментирует часть запроса с проверкой пароля. В качестве пароля в форме тогда можно ввести что угодно (например, 123).
# Подумайте, почему такой  запрос возвращает данные администратора, не требуя правильного пароля.

import sqlite3

def login_to_system(user_login, user_password):
    # 1. Подключение к базе данных
    conn = sqlite3.connect('4.3.1.db')
    cursor = conn.cursor()

    # 2. Формируем НЕБЕЗОПАСНЫЙ запрос через f-строку
    # ВНИМАНИЕ: Это пример того, как ДЕЛАТЬ НЕЛЬЗЯ в реальных проектах!
    query = f"SELECT * FROM users WHERE login = '{user_login}' AND password = '{user_password}'"
  
    # 3. Выполнение запроса
    cursor.execute(query)
    result = cursor.fetchone()
  
    conn.close()
  
    # Возвращаем результат (данные пользователя или None)
    return result

# Пример использования для демонстрации атаки:
# Если ввести login = "admin' -- ", запрос превратится в:
# SELECT * FROM users WHERE login = 'admin' -- ' AND password = '...'
# Все, что после --, станет комментарием и пароль не проверится.
if __name__ == "__main__":
    login = input("Введите логин: ")
    password = input("Введите пароль: ")
    user = login_to_system(login, password)
  
    if user:
        print(f"Доступ разрешен! Данные пользователя: {user}")
    else:
        print("Ошибка: неверный логин или пароль.")
