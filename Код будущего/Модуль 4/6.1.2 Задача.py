# Создайте скрипт, который отправляет длинную строку в уязвимое приложение для проверки возможности возникновения переполнения буфера.

#### Пояснение к задаче
# 1. Импортируйте необходимый модуль для запуска внешних приложений и взаимодействия с ними.
# 2. Сформируйте длинную строку, состоящую из 5000 символов (например, можно 5000 раз повторить символ `'A'`).
# 3. Путь к исполняемому файлу уязвимого приложения этот каталог выполнения вашего скрипта (./vulnerable_app.exe).
# 4. Используй `subprocess.run()` и установи необходимые параметры
# 5. Обработай исключение `subprocess.TimeoutExpired`

import subprocess

def test_buffer_overflow():
    # 1. Формируем длинную строку (5000 символов 'A')
    payload = 'A' * 5000
  
    # 2. Путь к приложению
    app_path = r"./vulnerable_app.exe"
  
    print(f"Отправка payload длиной {len(payload)} символов в {app_path}...")

    try:
        # 3. Запуск приложения с передачей payload через ввод (input)
        # Устанавливаем таймаут, так как переполнение может подвесить программу
        result = subprocess.run(
            [app_path],
            input=payload,
            text=True,
            capture_output=True,
            timeout=5
        )
      
        print("Приложение завершило работу штатно.")
        print(f"Вывод: {result.stdout}")

    except subprocess.TimeoutExpired:
        # 4. Обработка исключения таймаута
        print("Ошибка: Приложение зависло (возможно, произошло переполнение буфера).")
    except FileNotFoundError:
        print(f"Ошибка: Файл {app_path} не найден.")
    except Exception as e:
        print(f"Произошла непредвиденная ошибка: {e}")

if __name__ == "__main__":
    test_buffer_overflow()
