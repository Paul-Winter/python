# Выполняя задачу 1 вы создали уязвимый скрипт, который позволяет обойти аутентификацию.
# Сейчас нужно исправить свои ошибки, сделав запрос безопасным с помощью параметризации.
# Напомним, проверка должна быть оформлена в нем в виде функции login_to_system(user_login, user_password).
# Функция должны возвращать данные пользователя. В рамках этой задачи работайте с файлом базы данных 4.3.2.db

#### Пояснение к задаче
# 1. По сравнению с предыдущей задачей измените формирование запроса: данные должны передаваться отдельно от самого запроса
# 2. Используйте знаки вопроса ? как места подстановки параметров
# 3. Передайте параметры вторым аргументом в execute() в виде кортежа
# 4. Возвратите данные пользователя при помощи return

#### Примечание
# - При передаче параметров отдельно, они воспринимаются SQLite только как данные, а не как часть кода запроса. Это предотвращает инъекции.
# - Теперь даже если пользователь введет специальные символы, они не сломают структуру запроса

import sqlite3

def login_to_system_safe(user_login, user_password):
    # 1. Подключение к базе данных 4.3.2.db
    conn = sqlite3.connect('4.3.2.db')
    cursor = conn.cursor()

    # 2. Используем безопасную параметризацию через знак вопроса
    # Это отделяет логику SQL от пользовательских данных
    query = "SELECT * FROM users WHERE login = ? AND password = ?"
  
    # 3. Передаем данные вторым аргументом в виде кортежа
    cursor.execute(query, (user_login, user_password))
    result = cursor.fetchone()
  
    conn.close()
  
    return result

if __name__ == "__main__":
    login = input("Введите логин: ")
    password = input("Введите пароль: ")
    user = login_to_system_safe(login, password)
  
    if user:
        print(f"Доступ разрешен! Данные пользователя: {user}")
    else:
        print("Ошибка: неверный логин или пароль.")
