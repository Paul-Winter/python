import sqlite3

def search_products(product_name):
    # Подключение к базе данных 4.3.3.db
    conn = sqlite3.connect('4.3.3.db')
    cursor = conn.cursor()

    # УЯЗВИМЫЙ ЗАПРОС: подставляем ввод напрямую
    # В таблице products три столбца: id, name, price
    query = f"SELECT id, name, price FROM products WHERE name = '{product_name}'"
  
    try:
        cursor.execute(query)
        results = cursor.fetchall()
    except Exception as e:
        results = f"Ошибка SQL: {e}"
  
    conn.close()
    return results

