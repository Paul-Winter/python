# Создайте скрипт, который проверяет заданный диапазон портов на доступность для локального хоста.

#### Пояснение к задаче
# 1. Импортируйте модуль `socket` для работы с сетевыми соединениями.
# 2. Установите таймаут ожидания соединения в 1 секунду с помощью `socket.setdefaulttimeout(1)`.
# 3. Список портов для проверки прочитайте из файла `ports.txt`.
# 4. Разделите строку по символу `'-'` и преобразуй полученные значения в целые числа,
# чтобы получить начальный и конечный порты диапазона.
# Не забудьте перед этим удалить лишние пробелы и символы перевода строки.
# 5. Проверьте все порты из списка на локальном хосте (`'127.0.0.1'`):
#     - Для каждого порта создайте сокет с параметрами `socket.AF_INET` (IPv4) и `socket.SOCK_STREAM` (TCP);
#     - Попробуйте подключиться к хосту и порту с помощью `connect_ex()`.
# Метод возвращает `0`, если подключение успешно, и код ошибки в противном случае;
#     - Если результат равен `0`, выведите сообщение: `f'Порт {port} открыт'`;
#     - Не забывайте закрывать сокет с помощью `close()` после проверки каждого порта.


import socket

def scan_port_range(filename):
    # 2. Установка глобального таймаута
    socket.setdefaulttimeout(1)
  
    try:
        # 3. Чтение файла
        with open(filename, 'r', encoding='utf-8') as file:
            content = file.read()
      
        # 4. Очистка строки
        content = content.strip()
      
        # 5. Получение диапазона
        start_port, end_port = map(int, content.split('-'))
      
        # 6. Целевой хост
        target = '127.0.0.1'
      
        # 7. Перебор портов (включительно)
        for port in range(start_port, end_port + 1):
            # 8. Создание сокета
            s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
          
            # 9. Проверка порта
            result = s.connect_ex((target, port))
          
            # 10. Вывод результата
            if result == 0:
                print(f"Порт {port} открыт")
          
            # 11. Закрытие сокета
            s.close()
          
    except FileNotFoundError:
        print(f"Ошибка: Файл {filename} не найден")
    except Exception as e:
        print(f"Произошла ошибка: {e}")

if __name__ == "__main__":
    # Указываем путь согласно ТЗ
    scan_port_range('ports.txt')
