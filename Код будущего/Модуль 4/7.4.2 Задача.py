# Создайте скрипт, который анализирует файл лога сервера и выявляет часто встречающиеся коды ошибок (например, HTTP 404, 500 и т.д.).

#### Пояснение к задаче
# 1. Импортируйте класс `Counter` из модуля `collections` и модуль `re` для работы с регулярными выражениями.
# 2. Откройте файл `'server.log'` в режиме чтения.
# 3. Прочитайте все строки файла.
# 4. Создайте пустой список `errors` для хранения найденных кодов ошибок.
# 5. Используйте регулярное выражение для поиска кодов ошибок:
#    `r'\b[45]\d{2}\b'` — находит трехзначные коды, начинающиеся с цифры 4 или 5 (коды ошибок клиента и сервера).
# 6. Для каждой строки лога выполните следующие действия:
#    - Используйте `re.findall()` для поиска всех совпадений с шаблоном в строке.
#    - Добавьте найденные коды ошибок в список `errors` с помощью `extend()`.
# 7. Создайте объект `Counter` из списка `errors` для подсчета частоты встречаемости каждого кода ошибки.
# 8. Переберите элементы счетчика с помощью `most_common()` и выведите результат в формате:
#    `"HTTP {код}: {количество} раз"`.

import re
from collections import Counter

def analyze_server_errors(filename):
    # 4. Список для хранения кодов ошибок
    errors = []
  
    # 5. Регулярное выражение: трехзначные коды на 4 или 5
    error_pattern = r'\b[45]\d{2}\b'
  
    try:
        # 2. Открытие файла
        with open(filename, 'r', encoding='utf-8') as file:
            # 3. Чтение строк
            for line in file:
                # 6. Поиск всех совпадений в строке
                found_codes = re.findall(error_pattern, line)
                # Добавление в список
                errors.extend(found_codes)
      
        # 7. Подсчет частоты
        error_counts = Counter(errors)
      
        # 8. Вывод результата (от самых частых к редким)
        for code, count in error_counts.most_common():
            print(f"HTTP {code}: {count} раз")
          
    except FileNotFoundError:
        print(f"Ошибка: Файл {filename} не найден")

if __name__ == "__main__":
    analyze_server_errors('server.log')
