# В файле 4-2-4.csv содержатся данные о сетевом трафике: время (time) и объем переданных данных в байтах (bytes).
# Ваша задача – статистически выявить аномалии – моменты, когда трафик резко превышает обычные значения.
# Для этого проанализируйте файл, используя Pandas:
# * Загрузите данные сетевого трафика (столбцы: time, bytes).
# * Рассчитайте стандартное отклонение и медиану объема трафика.
# * Выделите строки, где трафик превышает медиану на 3 сигмы.

#### Пояснения к задаче
# Считайте файл при помощи Pandas
# Преобразуйте время из формата “строка” во временной формат datetime (это потребуется для корректного вывода результатов)
# Рассчитайте для столбца bytes:
# Медиану (median()) – это "нормальный" уровень трафика.
# Стандартное отклонение (std()) – показатель разброса значений.
# Определите пороговое значение: медиана + 3 * стандартное отклонение. Всё, что выше этого порога, считается аномалией ("3 сигмы")
# Выделите строки с аномалиями и выведите их на экран в формате: время, объем данных в байтах.

import pandas as pd

# 1. Загрузка данных
# Файл содержит колонки time и bytes
df = pd.read_csv('4-2-3.csv')

# 2. Преобразование времени в формат datetime
df['time'] = pd.to_datetime(df['time'])

# 3. Расчет статистических показателей
# Медиана — это середина ряда, "типичное" значение
median_val = df['bytes'].median()
# Стандартное отклонение — мера разброса данных
std_val = df['bytes'].std()

# 4. Расчет порога по правилу "трех сигм"
threshold = median_val + 3 * std_val

# 5. Выделение аномалий
# Фильтруем строки, где значение байт выше порога
anomalies = df[df['bytes'] > threshold]

# 6. Вывод результатов
print(f"Порог аномалии (Медиана + 3*Std): {threshold:.2f} байт")
print("-" * 50)
if not anomalies.empty:
    print("Обнаружены аномалии в трафике:")
    # Выводим только нужные столбцы
    for index, row in anomalies.iterrows():
        print(f"Время: {row['time']}, Объем: {row['bytes']} байт")
else:
    print("Аномалий не обнаружено.")
