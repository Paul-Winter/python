# Реализуйте функцию caesar_cipher для шифрования и дешифрования текста на английском языке с использованием шифра Цезаря
# со сдвигом вправо, используя функцию ord для преобразования буквы в число.
# Функция должна вызываться следующим образом: caesar_cipher(text, key, mode), где text – текст для шифровки или расшифровки,
# key – значение ключа, mode – направление шифрования. Если при вызове передается 'encrypt',
# значит сообщение должно быть зашифровано, если 'decrypt', то расшифровано

#### Пояснение к задаче
# Напомним, что шифр Цезаря (лат. Notae Caesarianae), также известный как шифр сдвига или код Цезаря — разновидность
# шифра подстановки, в котором каждый символ в открытом тексте заменяется символом, находящимся на некотором
# постоянном числе (именуемом ключом) позиций левее или правее него в алфавите.

#### Основные правила:
#  - Игнорировать неалфавитные символы
#  - Сдвинуть буквы исходного текста на заданное число (ключ), например с ключом 3 получится ABC → DEF

#### Важно! Если сдвинуть последние буквы алфавита некуда, то алфавит начинается сначала. Т.е. после Z идет A

def caesar_cipher(text, key, mode='encrypt'):
    """
    Функция для шифрования и дешифрования текста шифром Цезаря.
    text - исходная строка
    key - число (сдвиг)
    mode - режим ('encrypt' или 'decrypt')
    """
    # Если режим дешифрования, просто меняем знак ключа
    if mode == 'decrypt':
        key = -key

    result = ""

    for char in text:
        if char.isalpha():
            # Определяем базу (65 для A, 97 для a)
            start = ord('A') if char.isupper() else ord('a')
    
            # Вычисляем сдвиг:
            # 1. Приводим код буквы к диапазону 0-25 (ord(char) - start)
            # 2. Добавляем ключ
            # 3. Берем остаток от деления на 26 для зацикливания
            new_pos = (ord(char) - start + key) % 26
    
            # Превращаем обратно в символ
            result += chr(start + new_pos)
        else:
            # Игнорируем (оставляем как есть) неалфавитные символы
            result += char
    
    return result
