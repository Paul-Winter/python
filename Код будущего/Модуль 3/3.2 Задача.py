from scapy.all import rdpcap, IP

# Читаем предзаписанный файл трафика
packets = rdpcap('capture.txt')

# Анализируем первые 5 пакетов
for i, pkt in enumerate(packets[:5], 1):
    proto = pkt.name
    # Проверяем наличие IP-слоя
    if pkt.haslayer(IP):
        src = pkt[IP].src
    else:
        src = "No IP"

    print(f"Пакет {i}: Протокол - {proto}, Источник - {src}")
