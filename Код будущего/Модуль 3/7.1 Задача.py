# Напишите логику, которая ограничивает количество попыток неудачных входов логина и пароля. Максимальное число попыток должно составлять 3

#### Пояснение к задаче
# Написанный код должен состоять только из одной функции login
# Эта функция должна получать на вход значения логина и пароля, флаг их правильности (1 – правильно, 0 – не правильно)
# Функция должна возвращать число попыток и статус, показывающий разрешен ли дальнейший ввод
# Кроме этой функции, код больше ничего содержать не должен
# Пример объявления такой функции: def login(login_val, password_val, is_correct, current_attempts)

# Функция login принимает:
# login_val - логин пользователя
# password_val - пароль пользователя
# is_correct - флаг (1 - верно, 0 - неверно)
# current_attempts - текущее число уже сделанных попыток

def login(login_val, password_val, is_correct, current_attempts):
    # Если попыток уже 3 или больше, ввод запрещен
    if current_attempts >= 3:
        return current_attempts, "Запрещено"
    if is_correct == 1:
    # Если вход верный, счетчик сбрасывается (или остается 0)
        return 0, "Разрешено"
    else:
    # Если вход неверный, увеличиваем счетчик
        new_attempts = current_attempts + 1
        status = "Разрешено" if new_attempts < 3 else "Запрещено"
        return new_attempts, status
