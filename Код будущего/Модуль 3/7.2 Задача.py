# Напишите логику, которая обеспечивает фильтрацию данных, полученных при вводе логина и пароля так,
# чтобы было невозможно произвести SQL-инъекцию.

#### Пояснение к задаче
# Написанный код должен состоять только из одной функции generate_secure_query
# Эта функция должна получать на вход значения логина и пароля, а возвращать их же, но обработанные так,
# чтобы было невозможно сделать SQL-инъекцию
# Для решения воспользуйтесь методом “экранирования”
# Кроме этой функции, код больше ничего содержать не должен
# Пример объявления такой функции: generate_secure_query(username, password)

def generate_secure_query(user_login, user_pass):
    """
    Формирует SQL-запрос для проверки пользователя,
    защищая его от обхода авторизации.
    """
    # 1. Экранируем одинарные кавычки, чтобы они не разрывали строку в SQL
    # Заменяем одну кавычку (') на две (''), что в SQL является стандартом экранирования
    safe_login = user_login.replace("'", "''")
    safe_pass = user_pass.replace("'", "''")

    # 2. Формируем финальный запрос
    # Используем очищенные переменные внутри кавычек
    query = f"SELECT * FROM users WHERE login = '{safe_login}' AND password = '{safe_pass}';"

    return query
