# Напишите функцию validate_russian_email(email) для проверки корректности ввода email-адреса с проверкой того,
# что этот адрес относится к российскому национальному пространству доменных имен.

#### Пояснение к задаче
# Адрес электронной почты должен соответствовать следующим условиям:
#  - Email должен содержать символ @ и точку в доменной части
#  - Допустимые символы для имени (до символы @): буквы (a-z, A-Z), цифры (0-9), точки (.), дефисы (-), подчеркивания (_)
#  - Допустимые символы для домена (после символа @): буквы (a-z, A-Z, а-я, А-Я), цифры (0-9), точки (.), дефисы (-), подчеркивания (_)
#  - К российскому национальному пространству доменных имен относятся доменные зоны .РФ, .RU, .SU
#  - Возвращайте значение "ложь", если адрес не прошел валидацию (return False) и “истина” (return True), если прошел успешно
# Примеры корректных email: user.name@example.ru, ivan2026@домен.рф.

import re

def validate_russian_email(email):
    """
    Проверяет email на соответствие стандартам и принадлежность
    к российскому доменному пространству (.RU, .SU, .РФ).
    """
    # Регулярное выражение:
    # 1. Локальная часть (до @): буквы a-z, цифры, точки, дефисы, подчеркивания.
    # 2. Доменная часть (после @): буквы (лат + кир), цифры, точки, дефисы.
    # 3. Зона: только .ru, .su или .рф (регистронезависимо).

    pattern = r"^[a-zA-Z0-9._-]+@([a-zA-Z0-9а-яА-ЯёЁ._-]+\.(ru|su|рф))$"

    # Используем re.IGNORECASE для поддержки .RU, .РФ и т.д.
    match = re.match(pattern, email, re.IGNORECASE)

    if match:
        return True, "Email корректен и относится к зоне RU/SU/РФ"
    else:
        return False, "Неверный формат или доменная зона не входит в пространство РФ"
