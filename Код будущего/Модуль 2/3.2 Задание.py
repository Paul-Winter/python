# Напишите программу, которя выводит топ-3 IP-адреса с наибольшим количеством
# уязвимостей на основе файла vulns.log

# Требования:
# 1. Программа не должна ничего запрашивать у пользователя.
#    Она должна сразу начать обработку файла vulns.log,
#    который расположен в той же директории.
# 2. Программа должна открыть файл vulns.log в кодировке utf-8 и построчно
#    прочитать его содержимое.
# 3. В каждой строке файла первым элементом является IP-адрес.
#    Это первое слово в строке. Остальная часть строки не используется.
# 4. Нужно подсчитать, сколько раз каждый IP встречается в файле, и
#    вывести три IP-адреса, которые встречаются чаще всего.
# 5. Вывод должен содержать ровно три строки - топ-3 IP по частоте.
#    Формат строки: IP: количество.
# 6. Порядок вывода строго соответствует убыванию количества уязвимостей -
#    первый (самый частый), затем второй, затем третий.
#    При равенстве количества порядок определяется внутренней сортировкой
#    Counter.most_common(), т.е. тест ожидает именно такой порядок.
# 7. Пустые строки должны игнорироваться.
# 8. Если файл vulns.log отсутствует:
#  - вывести сообщение: Файл vulns.log не найден
#  - программа не должна падать
#  - stderr должен быть пустым
# 9. Программа должна печатать результат только через print.
#    stderr всегда пустой.
# 10. Название файла программы строго index.py.
#    Имя обрабатываемого файла строго vulns.log.

from collections import Counter

def top3_ips(log_file="vulns.log"):
    try:
        ips = []
        with open(log_file, "r", encoding="utf-8") as f:
            for line in f:
                parts = line.strip().split()
                if parts:
                    ips.append(parts[0])
        counter = Counter(ips)
        for ip, count in counter.most_common(3):
            print(f"{ip}: {count}")
    except FileNotFoundError:
        print(f"Файл {log_file} не найден")

if __name__ == "__main__":
    top3_ips()