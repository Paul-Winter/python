# Пример программы для чтения CSV-файла с помощью Pandas и вывода первых 5 строк.

# 1. Прочитать CSV-файл sales.csv и загрузить данные.
# 2. Проверить данные на наличие пропусков и удалить их, если они есть.
# 3. Преобразовать типы данных: столбец "Количество" в целые числа,
#    столбец "Цена" в числа с плавающей точкой.
# 4. Добавить новый столбец "Выручка", который вычисляется как произведение
#    "Количество" на "Цена".
# 5. Посчитать и вывести суммарную выручку по всем товарам.
# 6. Определить и вывести самый популярный товар по количеству проданных единиц.
# 7. Посчитать и вывести выручку по регионам.
# 8. Сохранить итоговый DataFrame в новый CSV-файл sales_report.csv.
# 9. Если файла sales.csv нет, вывести сообщение: Файл sales.csv не найден.

import pandas as pd

input_file = "sales.csv"
output_file = "sales_report.csv"

try:
    # 1. Загрузка и очистка данных
    df = pd.read_csv(input_file)
    df.dropna(inplace=True) # удаление пропусков

    # 2. Изменение типов данных
    df['Количество'] = df['Количество'].astype(int)
    df['Цена'] = df['Цена'].astype(float)

    # 3. Добавление столбца "Выручка"
    df['Выручка'] = df['Количество'] * df['Цена']

    # 4. Суммарная выручка по товарам
    total_revenue = df['Выручка'].sum()
    print(f"Суммарная выручка: {total_revenue:.2f}")

    # 5. Самый популярный товар
    popular_product = df.groupby('Товар')['Количество'].sum().idxmax()
    print(f"Самый популярный товар: {popular_product}")

    # 6. Выручка по регионам
    revenue_by_region = df.groupby('Регион')['Выручка'].sum()
    print("Выручка по регионам:")
    print(revenue_by_region)

    # 7. Сохранение результатов
    df.to_csv(output_file, index=False)
    print(f"Результаты сохранены в {output_file}")
except FileNotFoundError:
    print(f"Файл {input_file} не найден")
