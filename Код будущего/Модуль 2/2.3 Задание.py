# напишите программу-калькулятор в файле index.py

# Программа должна строго следовать следующим правилам:
# 1. Программа считывает три строки через input():
#  - первая строка - первое число
#  - вторая строка - второе число
#  - третья строка - операция (+,-,*,/)
# 2. Программа выполняет указанную операцию и выводит результат через
#    print без лишних слов. Например, при вводе 5, 3 и + вывод должен
#    быть ровно: 8.0
# 3. Программа обязана обрабатывать ошибки и не должна
#    завершаться с исключениями
# 4. Все ошибки должны записываться в лог-файл calculator.log.
#    Логирование должно быть настроено с уровнем ERROR.

# Правила обработки ошибок:
# 1. Некорректный ввод (например, abc вместо числа):
#  - вывести: Ошибка: введите числа
#  - в calculator.log должна быть запись, содержащая фразу
#    "введены некорректные данные"
# 2. Деление на ноль:
#  - вывести: Ошибка: деление на ноль
#  - в calculator.log должна быть запись, содержащая фразу
#    "деление на ноль"
# 3. Неизвестная операция (не +,-,*,/):
#  - вывести: Неизвестная операция
#  - в лог-файл писать ничего нельзя

# Дополнительные требования:
# - stderr должен быть пустым, вся информация выводится только через print
# - при успешной операции лог-файл calculator.log не должен содержать ошибок
# - лог-файл создаётся только в случае ошибки
# - название основного файла строго index.py
# - название лог-файла строго calculator.log

# Программа должна полностью удовлетворять этим требованиям, чтобы
# корректно пройти автоматические тесты.

import logging

logging.basicConfig(filename='calculator.log', level=logging.ERROR)

def calculator():
    try:
        num1 = float(input())
        num2 = float(input())
        operation = input().strip()

        if operation == '+':
            print(num1 + num2)
        elif operation == '-':
            print(num1 - num2)
        elif operation == '*':
            print(num1 * num2)
        elif operation == '/':
            if num2 == 0:
                raise ZeroDivisionError("Деление на ноль")
            print(num1 / num2)
        else:
            print("Неизвестная операция")
    except ValueError:
        logging.error("Ошибка: введены некорректные данные")
        print("Ошибка: введите числа")
    except ZeroDivisionError:
        logging.error("Ошибка: деление на ноль")
        print("Ошибка: деление на ноль")
    except Exception as e:
        logging.error(f"Неожиданная ошибка: {e}")
        print("Произошла ошибка")

calculator()
