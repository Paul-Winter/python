# чтение файла с защитой от ошибок:
# - написать программу, которая запрашивает у пользователя имя файла и
#   выводит его содержимое построчно
# - если файл не найден - вывести сообщение об ошибке и записать это
#   событие в лог-файл

# Программа должна строго следовать следующим правилам.
# 1. Программа считывает имя файла через input() без подсказок и дополнительного текста.
#    Имя файла считывается одной строкой.
# 2. Программа пытается открыть файл с указанным именем и выводит его содержимое построчно.
#    Каждая строка файла должна выводиться через print без лишних слов и без добавления лишних символов.
#    Концы строк должны обрабатываться корректно: вывод должен совпадать с содержимым файла.
# 3. Программа обязана обрабатывать ошибки и не должна завершаться с исключениями.
# 4. Все ошибки должны записываться в лог-файл file_reader.log.
#    Логирование должно быть настроено с уровнем ERROR и кодировкой utf-8.

# Правила обработки ошибок:
# 1. Если файл не найден (FileNotFoundError):
#  - вывести: Ошибка: файл не найден
#  - в file_reader.log должна появиться запись, содержащая фразу "файл не найден"
# 2. Любая другая ошибка:
#  - вывести: Произошла ошибка
#  - в лог-файл должна быть записана строка с описанием ошибки

# Дополнительные требования:
# - stderr должен быть пустым, вся информация выводится только через print
# - при успешном чтении лог-файл file_reader.log не должен содержать сообщений об ошибках
# - лог-файл должен создаваться только в случае ошибки
# - название основного файла строго index.py
# - название лог-файла строго file_reader.log

import logging

logging.basicConfig(filename="file_reader.log", level=logging.ERROR, encoding="utf-8")

def read_file():
    try:
        filename = input().strip()
        with open(filename, "r", encoding="utf-8") as f:
            for line in f:
                print(line.rstrip())
    except FileNotFoundError:
        logging.error("Ошибка: файл не найден")
        print("Ошибка: файл не найден")
    except Exception as e:
        logging.error(f"Неожиданная ошибка: {e}")
        print("Произошла ошибка")

read_file()
