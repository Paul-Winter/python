# Напишите программу в файле, которая подсчитывает количество уязвимостей
# для каждого IP-адреса под данным из файла vulns.log

#    Требования к программе:
# 1. Программа не должна запрашивать ввод у пользователя.
#    Она должна сразу начать обработку файла vulns.log, который находится
#    в той же директории.
# 2. Программа должна открыть файл vulns.log в кодировке utf-8 и построчно
#    прочитать его содержимое.
# 3. В каждой строке файла первым элементом является IP-адрес.
#    Строка может содержать дополнительные данные, но они игнорируются.
#    IP-адрес - это первое слово в строке.
# 4. Нужно подсчитать, сколько раз каждый IP встречается в файле.
#    Подсчёт выводится в формате: IP: количество
#    Выводить каждую строку через print.
# 5. Пустые строки или строки без данных должны пропускаться.
# 6. Если файл vulns.log отсутствует:
#  - вывести сообщение: Файл vulns.log не найден
#  - программа не должна падать с исключением
#  - stderr должен быть пустым
# 7. Все сообщения должны выводиться только через print. stderr пустой.
# 8. Порядок вывода IP-адресов может быть любой.
# 9. Название файла программы строго index.py.
#    Имя файла, из которого читаются данные, строго vulns.log.

log_file="vulns.log"
counts = {}
try:
    with open(log_file, "r", encoding="utf-8") as f:
        for line in f:
            parts = line.strip().split()
            if not parts:
                continue
            ip = parts[0]
            counts[ip] = counts.get(ip, 0) + 1
    
    for ip, total in counts.items():
        print(f"{ip}: {total}")
except FileNotFoundError:
    print(f"Файл {log_file} не найден")
