# Переопределите метод len для нахождения длины списка/строки,
# чтобы он выводил всегда число 25

# 1. Создать класс Always25.
# 2. В классе переопределить метод __len__.
# 3. Метод __len__ должен всегда возвращать число 25,
#    независимо от содержимого объекта.
# 4. Не использовать запрещённые конструкции: import, eval, exec, subprocess.
# 5. Проверить, что при вызове len() для экземпляра класса возвращается 25.

class Always25:
    def __len__(self):
        return 25
